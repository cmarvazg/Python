#Datos de tipo cadena
nombre=input("Digite su nombre: ")
print(f"Hola {nombre}",type(nombre))

#Datos numéricos
numero=int(input("Digita un número entero: "))
print(f"El número que digitaste fue {numero}",type(numero))
numero=float(input("Digita un número flotante: "))
print(f"El número que digitaste más 3 es {numero+3}",type(numero))#Se puede operar en medio de {}
