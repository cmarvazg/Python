#En python existen tres tipos de datos
#Numéricos int/float...
num1=10
num2=5
resultado=(num1+num2)/2#Los operadores respetan la jerarquia matemática
print("El resultado de ",num1,"+",num2,"/ 2 =",resultado)
print(type(resultado))#La función type sirve para indicar el tipo de variable ()

#Caracteres str
cadena='Hola "mundo"'#Se pueden usar '' o "" o mezclarlas
print(cadena)
print(type(cadena))

#Booleanos bool
valor=True#La primer letra debe ser mayuscula
print(valor)
print(type(valor))
